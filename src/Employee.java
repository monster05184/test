
public class Employee {
private int ID;
private String name;
public String toString() 
{
	return ID+name;
}
public int getID()
{
	return ID;
}
public void setID(int ID)
{
	this.ID=ID;
}
public String getname()
{
	return name;
}
public void setname(String name)
{
	this.name=name;
}
}
